    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        
        Double a,b,c,x1,x2,delta,r;
        
        a=Double.parseDouble(valeurUn.getText());
        b=Double.parseDouble(valeurDeux.getText());
        c=Double.parseDouble(valeurTrois.getText());
        
        delta=b*b-4*a*c;
        
        if (delta>=0)
        {
           x1=(-b-Math.sqrt(delta))/(2*a);
        x2=(-b+Math.sqrt(delta))/(2*a);
        
        
        jTextField1.setText(Double.toString(x1));
        jTextField2.setText(Double.toString(x2));
      
        }else  {
        
       
         JOptionPane.showMessageDialog(null, 
                              "No hay raices reales para la ecuaci√≥n", 
                              "Delta < 0", 
                              JOptionPane.WARNING_MESSAGE);
        }
        
            
       
        
    }//GEN-LAST:event_jButton1MouseClicked

